package calculator;

public class Calculator {
    static int add(int i, int j) {
        return i + j;
    }

    static int substract(int i, int j) {
        return i - j;
    }

    static int supply(int i, int j) {
        return i * j;
    }

    static int divde(int i, int j) {
        return i / j;
    }
}
